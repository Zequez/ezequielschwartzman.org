  <p id="#mundo-de-juego">
    <strong>Mundo de Juego:</strong>
    Un Mundo de Juego es un campo de compromiso creado por tres o más personas aplicando
    distinciones, software mental, y reglas-de-juego internamente-consistentes que
    emergen de un contexto acordado. Los Mundos de Juego pueden ser desde temporarios
    y con consecuencias insignificantes hasta largos y con consecuencias mayores.
    Ejemplos de Mundos de Juego incluyen: vamos a subir por el ascensor, vamos a
    tener una fiesta, vamos a tener una relación, vamos a tener una familia, vamos
    a tener una empresa, vamos a tener una ciudad, vamos a tener un país, vamos a
    tener una guerra, etc. Los Mundos de Juego pueden ser Responsables o Irresponsables,
    dependiendo de su Propósito y sus Resultados. Los Mundos de Juego se vuelven
    Defensivos si sus Historias son Creídas como verdad o se hacen invisibles a los
    participantes, en cuyo caso se dice que los participantes están identificados
    con su Mundo de Juego. Incluso si un Mundo de Juego es considerado como la una
    y única realidad por la mayoría de sus jugadores, es aún un Mundo de Juego. Los
    Mundos de Juego fundados en Creencias y Principios de la Sombra construyen una
    falsa comunidad y Sobreviven mediante la competencia con recursos limitados mediante
    la guerra. Los Mundos de Juego fundados en Principios Brillantes crean Resultados
    Responsables y construyen comunidad auténtica que prospera en colaboración creativa
    para originar los recursos. Tomar responsabilidad al nivel de ser el creador
    de Historias para Mundos de Juego es la puerta de entrada a Gestión de Posibilidades.
    Construir nuevos Mundos de Juego puede crear el espacio para activar tu Linaje
    Arquetípico. Para parafrasear a R. Buckmister Fuller, "No cambiás las cosas luchando
    contra los Mundos de Juego existentes. Cambiás las cosas creando nuevos Mundos
    de Juego, que hacen a los Mundos de Juego existentes obsoletos."

