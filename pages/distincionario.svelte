  <p id="#sostenedor-de-espacio">
    <strong>Sostenedor de Espacio:</strong> Sostener Espacio es el acto de ser Responsable
    al nivel de Espacio. El Espacio puede ser Declarado como una caja de ocho vértices,
    o como una burbuja amorfa. El espacio es sostenido con Atención y compromiso
    al Servicio, no mediante el control, dominación o manipulacióñ, que son Principios
    de la Sombra. Cuando un Espacio es Sostenido, el Espacio se llama a la existencia
    para servir a los Principios Brillantes. Una vez que podés Sostener Espacio,
    podés aprender a Navegar el Espacio. Sostener Espacio es una Habilidad Central.
