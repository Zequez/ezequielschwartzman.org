  <h1
    ><em class="font-mono font-size-6">The Edgeworker's</em><br /> Flowing Fund Gameworld
  </h1>
  <h2>New values</h2>
  <p>
    Having all-the-time access to large quantities of money accumulated can be
    undesirable as it can be distracting and make your Box harder to manage.
  </p>
  <p>Having a fresh steady flow of money is desirable.</p>
  <p>
    Deciding beforehand a sufficient amount of money and the purpose (where it
    will flow from you) of the money that will flow into you is desirable.
  </p>
  <p>
    Everybody being able to see what you do with the money you receive is
    desirable.
  </p>
  <p>
    Enabling people to commit inordinates amounts of money to your evolutinary
    path is desirable.
  </p>
  <h2>New distinctions</h2>
  <p>
    Some boxes are better equiped at managing large quantities of money than
    others.
  </p>
  <p>
    Different Box designs require different dynamics with money for them to work
    effectively.
  </p>
  <p>It's only possible to own money when it's given.</p>
  <h2>Purpose</h2>
  <p>
    Money has no value. Yet, making agreements with people about how money will
    flow, does have value; that's a facet Creative Collaboration; it's a way to
    signal and agree with others at where value is being created.
  </p>
  <p>
    The purpose of the Edgeworker's Flowing Fund Gameworld is to create the
    value of committing to each others committments at scale.
  </p>
  <h2>Rules of Engagement</h2>
  <p>Read this Codex before you start.</p>
  <p>Look at least another participant Flowing Fund Page.</p>
  <p>
    <strong>Choose</strong> a measurable time Cycle with a length.
  </p>
  <!-- prettier-ignore -->
  <p>
    You make an inventory of all the ways you are flowing money into others and
    all the ways you want to flow money into others, and then you make
    categories and <strong>decide how much money you want to flow into what at each
    Cycle Turning</strong>. You are invited to look at examples from other participants to be inspired.
  </p>
  <h3>Your Flowing Fund Guardian</h3>
  <p>
    You <strong>pick a Guardian</strong> for your Flowing Fund, a person who will
    hold the money and is responsible for transferring you the money you decided
    at each cycle.
  </p>
  <p>
    You may negotiate loans from the Flowing Fund with the Guardian, to be paid
    back automatically by discounting from next cycles.
  </p>
  <p>
    Your Guardian may negotiate loans with other participants using your Flowing
    Fund.
  </p>
  <p>
    You may negotiate loans with other participants Guardians using the Flowing
    Funds they are guarding.
  </p>
  <h3>Credits</h3>
  <p>
    If at a Cycle Turning there is not enough money on your Flowing Fund, you
    issue
    <strong>Credits</strong> for the remaining amount.
  </p>
  <h4>Issuing Credits</h4>
  <p>You will need:</p>
  <ul>
    <li>A piece of paper big enough</li>
    <li>Something to draw on the paper</li>
    <li>A timer</li>
  </ul>
  <p>Then create it:</p>
  <ol>
    <li>Write (Your Full Name)'s Flowing Fund Credit in the paper.</li>
    <li>Write the credited amount.</li>
    <li>Write your Flowing Fund Page web address.</li>
    <li>Make an empty square about 3cm by 3cm</li>
    <li>Set the timer to 30 minutes and draw on the square</li>
    <li>Take a picture of the credit issued</li>
    <li>Add the picture to your Flowing Fund Page on that Cycle</li>
  </ol>
  <p>
    With this the Credit is issued, and you can now give the paper to other
    people.
  </p>

  <h4>Redeeming Credits</h4>
  <p>
    You may redeem the Credit by negotiating with the Flowing Fund Guardian.
  </p>

  <p>
    Credits may be circulated and used as money in themselves until redeemed.
    Every time a credit changes hand, a signature is added.
  </p>

  <h3>Your Flowing Fund Page</h3>
  <p>
    You have a Flowing Fund page publicly available online. On this page the
    following information is made accessible:
  </p>
  <ul>
    <li>Your full real name and picture of you</li>
    <li>The full real name and picture of your Flowing Fund Guardian</li>
    <li>Your Flowing Fund Time Cycle</li>
    <li>
      Your current decision about how much money you receive at each Cycle
      Turning and how it flows
    </li>
    <li>The total amount of money in the Flowing Fund</li>
    <li>
      The list of all inputs of money into the Flowing Fund and from whom or
      where they came from
    </li>
    <li>Instructions about how to add money to your Flowing Fund</li>
    <li>Instructions about how to redeem credits</li>
    <li>
      A section for each Cycle Turning:
      <ul>
        <li>Cycle Turning date</li>
        <li>Amount received on that Cycle Turning</li>
        <li>
          Credits issued on that Turning, with pictures of the credit and wether
          the Credit has been redeemed
        </li>
        <li>
          Summary of how it when with the money you received on the previous
          Turning
        </li>
      </ul>
    </li>
  </ul>
